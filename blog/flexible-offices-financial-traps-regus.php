                    <h2 class="fw-bold mt-4 mb-3" style="color: #0D47A1;">A Growing Pattern of Complaints</h2>
                    <p>In recent months, CPB has received reports from small and medium-sized enterprises (SMEs) describing aggressive and unjustified demands from <strong>IW Group Services (UK) Ltd</strong> (trading as Regus), a global provider of flexible workspace solutions.</p>
                    <p>While Regus promotes itself as offering "flexible workspace solutions" for start-ups and entrepreneurs, evidence now emerging suggests that flexibility often stops the moment a business signs an online agreement.</p>
                    <p>The following case, which has been verified by CPB through email correspondence and invoices, illustrates the difficulties many small firms face when challenging misleading terms and automated billing systems.</p>

                    <h2 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">A Case in Point: CRC vs IW Group Services (UK) Ltd</h2>
                    <p>In May 2023, Compliance and Risk Consultancy Ltd (CRC) approached Regus to rent an office, making it explicitly clear that the company would not move in before 1 September 2023.</p>
                    <p>Regus's regional manager, Ms. Alina Craciun, confirmed during telephone discussions that this would be acceptable and that rent would commence only from 1 September. Relying on this assurance, CRC signed the digital agreement via DocuSign.</p>
                    <p>Just two days later, Regus issued an invoice backdated to April 2023, claiming arrears for months before the agreed start date. When CRC queried this, the company insisted that the system could not be changed and that rent was due immediately.</p>
                    <p>CRC then formally cancelled the agreement in writing, citing misrepresentation and lack of mutual consent. Regus rejected the cancellation, claiming there was "no cooling-off period" and threatening to charge the full 24-month term, over £50,000, even though the office was never occupied.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-warning mt-4">
                        <h5 class="fw-bold text-warning">Hidden Charges and Unexplained Fees</h5>
                        <p class="mb-0">A troubling revelation has surfaced from an insider, confirming that once a firm signs an agreement with Regus, the company automatically adds an additional £20 to the monthly rent for tea and coffee, which is supposedly included in the package. This charge is never disclosed prior to signing the agreement, leaving customers unaware of this added cost until after they have committed to the contract. This hidden fee further highlights the lack of transparency in Regus's billing practices, which many small businesses have found themselves unintentionally caught in.</p>
                    </div>

                    <h2 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">Three Debt Collectors, One Disputed Contract</h2>
                    <p>Since 2023, Regus has instructed three separate debt collection agencies to pursue the alleged debt. The latest, CCI Credit Management Ltd, issued an "urgent" demand in October 2025 warning of "further recovery proceedings."</p>
                    <p>The account remains formally disputed, yet collection efforts have continued, a practice that appears to conflict with the Financial Conduct Authority's (FCA) Consumer Credit Sourcebook (CONC), which requires that collection ceases immediately when a debt is contested.</p>
                    <p>This pattern raises questions about Regus's internal oversight and the due diligence of its appointed debt collectors, who are bound by FCA regulations to act fairly and proportionately.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-danger mt-4">
                        <h5 class="fw-bold text-danger mb-3">Legal and Ethical Implications</h5>
                        <ul class="ps-3">
                            <li><strong>Misrepresentation</strong> under the Misrepresentation Act 1967, given the verbal assurance that rent would begin only upon occupation.</li>
                            <li><strong>Unfair Contract Terms</strong> under the Unfair Contract Terms Act 1977 and Consumer Rights Act 2015, where small firms are locked into obligations without receiving any service.</li>
                            <li><strong>Breach of FCA Rules</strong>, particularly CONC 7.14.1R (requirement to suspend recovery during dispute) and CONC 7.3.4G (misleading enforcement threats).</li>
                            <li>Potential breaches of the <strong>UK GDPR</strong>, as repeated sharing of company data between multiple debt agencies may lack lawful basis once the contract is voided or rescinded.</li>
                        </ul>
                        <p class="mt-3">CRC's experience mirrors several complaints now reaching CPB from other small businesses, including one recent case involving a virtual office agreement, where the business was charged full rates despite never activating the service.</p>
                    </div>

                    <h2 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">IW Group Services (UK) Ltd's Reputation Under Scrutiny</h2>
                    <p><strong>IW Group Services (UK) Ltd</strong> operates under global brands such as Regus, Spaces, and HQ, promoting itself as a trusted partner for entrepreneurs and SMEs. However, mounting anecdotal evidence indicates that systemic inflexibility and opaque billing processes are creating financial distress for smaller clients.</p>
                    <p>Online forums and review platforms are replete with similar stories, small businesses trapped by inflexible digital contracts, denied cancellation, and threatened with substantial debts for services never delivered.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-success mt-4">
                        <h5 class="fw-bold text-success mb-3">CPB's Position and Projected Action</h5>
                        <p>CPB considers this a matter of significant public interest. Small firms form the backbone of the UK economy and should not be exposed to disproportionate pressure or legal intimidation for unconsumed services.</p>
                        <p>CPB is calling for:</p>
                        <ul class="ps-3">
                            <li>An <strong>FCA investigation</strong> into the debt collection conduct of firms acting on behalf of IW Group Services (UK) Ltd.</li>
                            <li>A <strong>Competition and Markets Authority (CMA)</strong> review of IW Group Services (UK) Ltd's contractual model with micro-businesses.</li>
                            <li>A <strong>sector-wide inquiry</strong> into digital contract practices within the flexible workspace industry.</li>
                        </ul>
                        <p class="mb-0">Affected businesses are encouraged to contact CPB confidentially at <strong>info@consumerprotectionbureau.co.uk</strong> with supporting documents.</p>
