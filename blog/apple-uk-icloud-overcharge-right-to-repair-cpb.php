                    Apple Faces Dual Pressure: iCloud Lawsuit and Repair Misconduct
                    Under Spotlight
                </h3>
                <p>
                    Apple is now at the crossroads of two major UK consumer challenges.
                    First, Which? launched a £3 billion legal claim in late 2024, accusing Apple of
                    using iOS’s dominance to push iCloud subscriptions—effectively trapping users
                    and overcharging them. The case
                    covers some 40 million UK users since October 2015, with potential
                    compensation of about £70 per person The . A
                    key hearing is scheduled for 19–21 November 2025 to determine class-action
                    certification .
                </p>

                <p>
                    Meanwhile, CPB’s Right to Repair campaign is uncovering troubling practices
                    at Apple’s UK service centres. A consumer was told their MacBook was
                    &quot;doomed&quot; and only fixable via a full logic board and storage replacement for
                    £1,700 (later “discounted” to £1,379), with no diagnostic report and used parts
                    likely used. Yet the device booted up the next day, undermining Apple’s own
                    evaluation.
                </p>
