                <p>
                    The Which? claim, filed on behalf of an estimated 40 million Apple customers,
                    alleges that Apple breached UK competition law by unfairly favouring its iCloud
                    storage service and overcharging users since 2015. According to Which?,
                    Apple has created a “trapped” customer base by making iCloud the default
                    and most convenient option, overcharging users by around £75 per year.
                </p>

                <p>
                    Apple strongly denies the allegations and has said it will “vigorously” defend
                    itself, insisting customers are free to choose other services and that its
                    pricing is competitive. The case, however, adds to growing scrutiny of Apple’s
                    practices, with the UK Competition and Markets Authority (CMA) already
                    investigating the iOS mobile ecosystem and the European Commission probing
                    App Store restrictions, including so-called “anti-steering” rules.
                </p>


                <p>
                    This latest challenge fits into a broader pattern of concern. CPB recently
                    highlighted Apple’s behaviour in the UK repair market, where consumers are
                    often told their devices are “beyond repair” and quoted exorbitant costs,
                    sometimes exceeding £1,700 without diagnostic transparency or affordable
                    alternatives. In many cases, replacement parts may not even be new. CPB has
                    since launched a global survey to gather evidence of consumer experiences with
                    Apple repairs, joining forces with Right to Repair coalitions across the EU and US.
                </p>

                <p>
                    Both cases reflect the same underlying issue: Apple’s ability to leverage its
                    market power to the detriment of consumer rights and fair competition. Whether
                    through repair restrictions or cloud storage lock-ins, the pattern suggests a
                    business model designed to maximise profits by limiting consumer choice.
                </p>

                <p>
                    For consumers, these legal and advocacy actions represent an important
                    opportunity to push back. CPB will continue to expose and challenge practices
                    that undermine transparency, fairness, and consumer choice, working with
                    partners worldwide to ensure accountability.
                </p>

