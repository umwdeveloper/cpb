                    The Dark Side of Digital Gift Card Marketplaces
                </h1>
                 <h2 class="fw-bold  mb-0 subtitle-bullet">A System Structurally Built to Fail Consumers
                    </h2>
                         <p class="text-muted mt-2 small mb-0 pb-0">Published by the <strong>Consumer Protection Bureau (CPB)</strong> | 25
