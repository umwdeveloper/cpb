                <div class="fs-5 lh-lg text-dark">
                    <p>British travellers are being unfairly penalised by airport "drop-off charges", a practice that amounts to double taxation on consumers and exposes deep flaws in the way motorists are treated at UK airports. The Consumer Protection Bureau (CPB) is calling for an immediate end to these unjust charges, which punish both drivers and passengers simply for reaching the terminal.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-primary mt-4">
                        <h5 class="fw-bold text-primary">A Modern Airport Toll in Disguise</h5>
                        <p class="mb-0">Most major UK airports now impose a charge of £5-£7 for dropping off a passenger, with penalties soaring to £100 or more for non-payment. These charges are managed by private parking operators, often through automatic number plate recognition (ANPR) systems, leaving travellers with no choice but to pay or risk a fine.</p>
                    </div>

                    <blockquote class="blockquote px-4 py-3 mt-4" style="background:#fff3cd; border-left: 5px solid #FFC107; border-radius: 6px;">
                        <p class="mb-0 fst-italic text-dark">This is not a service; it is a toll on access. Motorists already pay road tax, insurance premiums, and fuel duty. Passengers already pay airport tax as part of every ticket. Adding a separate fee to enter airport grounds is an unjustified layer of double taxation that hits families, carers, and ordinary workers the hardest.</p>
                    </blockquote>

                    <h3 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">A Hidden and Disproportionate Burden</h3>
                    <p>For many families, especially those with elderly relatives, young children, or passengers with disabilities, public transport alternatives are impractical. Drop-off zones are not a luxury; they are an essential part of airport access. Yet consumers are forced to deal with complex and poorly signposted payment systems, sometimes without mobile signal or clear instructions, and are penalised for honest mistakes.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-warning mt-4">
                        <h5 class="fw-bold text-warning">Real Impact on Real People</h5>
                        <p class="mb-0">In one case reported to CPB, Chris T. dropped a friend at Heathrow Terminal 3 and paid the £6 drop-off charge online the same day, only to receive a £100 Parking Charge Notice weeks later. His appeal to the private operator, APCOA, was rejected without explanation. Such experiences are not isolated; they reflect a broader pattern of opaque enforcement and consumer disadvantage.</p>
                    </div>

                    <h3 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">Where Regulation Fails</h3>
                    <p>Airports claim these fees reduce congestion and emissions, yet there is no credible evidence to support that claim. In reality, this is revenue generation disguised as regulation.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-danger mt-4">
                        <h5 class="fw-bold text-danger mb-3">Key Issues</h5>
                        <ul class="ps-3 list-unstyled">
                            <li>⬥ No transparency: Most airports fail to provide adequate pre-travel disclosure of fees.</li>
                            <li>⬥ No competition: Travellers cannot choose an alternative provider for drop-off access.</li>
                            <li>⬥ No accountability: Private operators often act as both enforcers and beneficiaries.</li>
                        </ul>
                    </div>

                    <p class="mt-4">These conditions create a monopoly environment, leaving motorists with no practical means to challenge unfair charges or recover wrongly issued fines.</p>

                    <div class="p-4 rounded bg-white border-start border-4 border-success mt-4">
                        <h5 class="fw-bold text-success mb-3">CPB's Position and Demands</h5>
                        <p>The Consumer Protection Bureau demands:</p>
                        <ul class="ps-3 list-unstyled">
                            <li>⬥ An immediate review by the Department for Transport and the Competition and Markets Authority into the fairness and proportionality of airport drop-off charges.</li>
                            <li>⬥ Transparency in all communications: airports must disclose charges clearly during booking, on websites, and at all entry points.</li>
                            <li>⬥ Abolition or drastic reduction of drop-off charges, recognising access to the terminal as a public right, not a chargeable commodity.</li>
                        </ul>
                    </div>

