                        class="text-muted small">Founder and Managing Director, Consumer Protection Bureau (CPB)</span>
                </p>

            </div>
            <div class="alert alert-info py-2 px-3 mb-4 small fst-italic"
                style="background:#e3f2fd; border-left:4px solid #1976d2;">
                Editor’s note: This article reflects the personal views and lived experience of the author. It is
                published as part of CPB’s wider work on consumer rights, informed consent, and regulatory
                accountability in healthcare. It does not constitute medical advice.
            </div>
            <div class="fs-5 text-dark" style="line-height:1.7;">
                <p class="fw-semibold">Over many years, I have observed a troubling shift in modern healthcare. Medication is increasingly
                    treated as the first response, not the last resort. People in pain, people deemed “at risk”, and
                    people with borderline clinical indicators are routinely placed on long-term drug regimens,
                    frequently without adequate exploration of alternatives, clinical judgement, or long-term
                    consequences.</p>
                <div class="alert alert-warning py-2 px-3 my-4"
                    style="background:#fffde7; border-left:4px solid #fbc02d;">
                    <strong>This is not a rejection of medicine.</strong> It is a call for medical restraint and ethical
                    clarity.
                </div>
                <p>The tendency is clear. We are moving from healing the sick to managing populations.</p>
                <div class="alert alert-secondary py-2 px-3 my-4"
                    style="background:#f3f6f9; border-left:4px solid #90caf9;">
                    <strong>This concern is not marginal.</strong> The World Health Organisation has repeatedly warned
                    about the global harms of over-medicalisation and polypharmacy, particularly among older adults and
                    people with chronic but stable conditions. Likewise, leading medical journals such as The Lancet and
                    the British Medical Journal have published sustained critiques of disease-definition creep, risk
                    inflation, and the tendency to treat probability as pathology.
                </div>
                <p>Disease definitions have expanded. Risk thresholds have been lowered. Normal ageing and moderate
                    discomfort are increasingly reframed as conditions requiring lifelong pharmaceutical management.</p>
                <p>In everyday clinical practice, pain is commonly pharmaceuticalised before non-drug interventions are
                    seriously considered. Borderline cholesterol, early respiratory symptoms, stress-related conditions,
                    or moderate musculoskeletal pain frequently result in immediate prescriptions, sometimes for life.
                </p>
                <p>What is rarely acknowledged is that many of these interventions are based on population-level
                    evidence. This means statistical risk reduction across large groups rather than clear, individual
                    pathology. What benefits a population on average may offer only marginal benefit to an individual,
                    while still carrying real burdens.</p>
                <div class="alert alert-danger py-2 px-3 my-4"
                    style="background:#ffebee; border-left:4px solid #d32f2f;">
                    <strong>Equally undeniable is the quiet influence of the pharmaceutical industry.</strong> This
                    culture did not emerge by accident.
                </div>
                <p>Industry-funded research, sponsored medical education, overseas conferences, travel grants, advisory
                    roles and scholarships have become normalised across healthcare systems worldwide. These
                    relationships are not always corrupt, but they are rarely neutral.</p>
                <p>Clinical guidelines increasingly rely on studies funded by the manufacturers of the very drugs being
                    recommended. The conflict of interest is structural, not incidental, and regulators can no longer
                    afford to treat it as benign.</p>
                <div class="alert alert-dark py-2 px-3 my-4" style="background:#ececec; border-left:4px solid #616161;">
                    <strong>The opioid crisis in the United States stands as a stark warning.</strong> As extensively
                    documented in both policy inquiries and peer-reviewed medical literature, it was not the product of
                    a single rogue company alone. It arose from regulatory capture, marketing excess, suppressed risk,
                    and a medical culture conditioned to trust pharmaceutical solutions over clinical scepticism. While
                    opioids represent an extreme case, the underlying dynamics are far more widespread.
                </div>
                <p>Fear remains a powerful driver. Patients warned of heart attacks, strokes, diabetes, or decline are
                    understandably anxious. In that emotional space, a prescription can feel like protection, even when
                    the long-term individual benefit is uncertain or marginal.</p>
                <p>Preventive prescribing exposes all patients to side effects in order to reduce risk for a small
                    proportion. Over time, dependency is reframed as “management”. Side effects generate further
                    prescriptions, and withdrawal becomes harder than the original condition.</p>
                <p>This approach quietly shifts the burden of systemic uncertainty onto individuals. Their bodies, their
                    daily lives, and their quality of living are compromised in the process.</p>
                <div class="alert alert-primary py-2 px-3 my-4"
                    style="background:#e3f2fd; border-left:4px solid #1976d2;">
                    <strong>It is time to call for clinical balance, transparency and reform.</strong>
                </div>
                <p>However, any credible, patient-centred healthcare system must:</p>
                <ul class="mb-4 list-unstyled" style="font-size:1.1rem;">
                    <li class="mb-2">(i) Treat medication as one tool among many, not the default
                        response;</li>
                    <li class="mb-2">(ii) Prioritise informed consent, lifestyle interventions and
                            non-pharmacological care;</li>
                    <li class="mb-2">(iii) Enforce full transparency around industry funding and
                        conflicts of interest; and</li>
                    <li>(iv) Re-establish regulatory independence and meaningful post-market
                        surveillance.</li>
                </ul>
                <div class="alert alert-success py-2 px-3 my-4"
                    style="background:#e8f5e9; border-left:4px solid #388e3c;">
                    <strong>Pain deserves compassion, not commercial exploitation.</strong> Also, risk deserves
                    monitoring, not monetisation.
                </div>
                <p>For policymakers and regulators, the challenge is clear. The question is no longer whether
                    over-medication exists, but why it continues to be tolerated.</p>
                <p class="fw-bold text-center mt-5 mb-0" style="font-size:1.2rem; color:#0D47A1;">Healthcare must return
                    to its core purpose: to serve patients, not markets.</p>
