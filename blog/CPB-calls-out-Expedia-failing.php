                <p>
                       A CPB member and his family endured unnecessary stress, contradictory communications, and weeks of frustration after two separate car rental bookings through Expedia in Bergamo, Italy went badly wrong.
                </p>



                <!-- Why it matters -->
                <h3 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">What Went Wrong
                </h3>
                <ul>
                    <li>
                        <b>Booking 1 (Autovia):</b> The rental desk refused to accept a debit card for deposit unless the consumer paid an additional €800 insurance. After consulting Expedia by phone, where a refund was promised during a recorded call, the customer cancelled at the counter. Expedia later refused the refund in writing, citing “terms and conditions.”
                    </li>
                    <li>
                       <b> Booking 2 (Sixt):</b> A request for a toddler seat was ignored, leaving a family with a three-year-old child stranded. Again, Expedia’s customer service assured a refund by phone, only to later deny it, citing “non-refundable” booking terms. The travel insurance bought alongside this booking was also denied a refund.
                    </li>
                </ul>
                <p>
                  Despite clear promises made during recorded calls, Expedia initially refused to refund either booking. Only after persistent pressure did the company eventually issue repayments — but with no explanation for the reversal or apology for the ordeal caused.
                </p>

                  <h3 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">Why It Matters
                </h3>
                <p class="text">This is not just about money. It is about trust, accountability, and consumer rights.</p>
                <ul>
                    <li>
                        Refunds should not require relentless chasing.
                    </li>
                    <li>
                       Recorded assurances from customer service must be honoured.
                    </li>
                    <li>
                        Safety-critical requests, such as child car seats, must be treated as essential, not optional extras.
                    </li>
                </ul>
                  <h3 class="fw-bold mt-5 mb-3" style="color: #0D47A1;">CPB’s Action
                </h3>
                <p class="text">CPB has written formally to Expedia to demand:</p>
                <ol>
                    <li>
                       A gesture of goodwill compensation to reflect the wasted time, stress, and disruption caused.
                    </li>
                    <li>
                      A clear statement of process improvements, including:
                      <ul>
                        <li>
                            Ensuring verbal assurances given on recorded calls are respected.
                        </li>
                        <li>
                            Better communication of essential booking requirements to rental partners.
                        </li>
                        <li>
                            An end to hiding behind “non-refundable” terms when consumer safety and rights are at stake.
                        </li>
                      </ul>
                    </li>
                  
                </ol>


                <blockquote class="blockquote px-4 py-3"
                    style="background:#fff3cd; border-left: 5px solid #FFC107; border-radius: 6px;">
