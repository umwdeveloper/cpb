                </h3>
                    <p class="mb-0 fst-italic text-dark">“Consumers should not have to struggle endlessly to obtain refunds that were already promised to them. In this case, a family was left stranded without essential child safety equipment and forced to endure weeks of contradictory responses from Expedia. While refunds have now been issued, the way the matter was handled is unacceptable. CPB is calling on Expedia to provide a gesture of goodwill compensation and to urgently review its processes so that no other family has to go through this ordeal.”</p>
